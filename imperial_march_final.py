def midiNoteToFreq(note):
    note_names = ['C', 'C#', 'D', 'D#', 'E', 'F', 'F#', 'G', 'G#', 'A', 'A#', 'B']
    
    pitch = note[:-1]
    octave = int(note[-1])
    
    note_index = note_names.index(pitch)
    midi_note_number = note_index + (octave + 1) * 12
    
    frequency = 440.0 * pow(2.0, (midi_note_number - 69) / 12.0) #432 is not correct, use 440
    
    return frequency

